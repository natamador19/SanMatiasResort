    private void btnagregarcotizacionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnagregarcotizacionMouseClicked
    String strRespuesta = "";
    
       try{
           ps = c.prepareStatement("select precio_cabaña,codigo_cabaña from cabañas where descripcion_cabaña = ?");
           ps.setString(1, cmbcabana.getSelectedItem().toString());
           rs = ps.executeQuery();
           while(rs.next()){
              precio_cabaña = (float) rs.getDouble("precio_cabaña");
              codigo_cabaña=rs.getInt("codigo_cabaña");
           }
           
           ps= c.prepareStatement("select capacidad_cabaña from cabañas where codigo_cabaña=?");
           ps.setInt(1, codigo_cabaña);
           rs = ps.executeQuery();
           while(rs.next()){
               cantidadP=rs.getInt("capacidad_cabaña");
           }
          
             strRespuesta = util.strPreguntaSiNo("¿Desea Ingresar la cotizacion?");
                  if (strRespuesta=="Si"){
                    
         
      JOptionPane.showMessageDialog(null,"Registro de datos exitosa");
           
            cantidadnoches=Integer.parseInt(txtcantidadnochescotizacion.getText());
            
           txttotal.setText(String.valueOf(Pagos(cantidadP,precio_cabaña)));
           txtSubtotal.setText(String.valueOf(subtotal));
           txtisv.setText(String.valueOf(Isv));
            
            ps=c.prepareStatement("call sanmatiasresort.INSERT_COTIZACION(?,?,?,?,?,?,?);");
            ps.setInt(1, is.empleado);
            ps.setInt(2, cmbcliente.getSelectedIndex());
            ps.setInt(3, codigo_cabaña);
            ps.setInt(4,cantidadP);
            ps.setFloat(5,personas);
            ps.setInt(6,cantidadnoches);
            ps.setFloat(7,precio_cabaña);
            ps.executeUpdate();
            mostrarTabla();
                 
            
            JOptionPane.showMessageDialog(null, "Registro Ingresado");
                  }
                  else{
                JOptionPane.showMessageDialog(null,"Registro No Ingresado");
           }
                
       } catch(SQLException ex) {
            Logger.getLogger(cotizacion.class.getName()).log(Level.SEVERE, null, ex);
       }

    }//GEN-LAST:event_btnagregarcotizacionMouseClicked

