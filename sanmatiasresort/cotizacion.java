    public void mostrarTabla(){
         DefaultTableModel modelo = new DefaultTableModel();
        rs = cn.LlenarTabla("SELECT * FROM sanmatiasresort.cotizaciones as A\n" +
                        "join detalle_cotizaciones as B on A.codigo_cotizacion=B.codigo_cotizacion");
       
        modelo.setColumnIdentifiers(new Object[]{
            "Código",
            "Codigo Empleado",
            "Codigo Cliente",
            "Cabaña",
            "cantidad personas",
            "costo extra",
          
            "cantidad_noches"
            
            
            
        });
        try{
            while(rs.next()){
                modelo.addRow(new Object[]{
                    rs.getString("codigo_cotizacion"),
                    rs.getString("codigo_empleado"),
                    rs.getString("codigo_cliente"),
                    rs.getInt("codigo_cabaña"),
                    rs.getInt("cantidad_personas"),
                    rs.getInt("costo_extra"),
                    
                    rs.getInt("cantidad_noches")
                   // rs.getString("cantidad_noches")
                });
            }
            tablacotizacion.setModel(modelo);
        }catch(SQLException e){
            System.out.println(e);
        }
    }
    



