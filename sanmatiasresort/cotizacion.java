 public double Pagos(int cantidadp, double preciocabaña){
        int resta;
        if(Integer.parseInt(txtcantidadpersonascotizacion.getText().toString())<=cantidadp){
            personas = 0;
            precio_extra=0;
        } else {
            resta = Integer.parseInt(txtcantidadpersonascotizacion.getText().toString())-cantidadp;
            personas= (float) (resta * Precio);
            
        }
        
        subtotal = (float) (precio_cabaña + personas + CantidadNoches());
        Isv = (float) (subtotal * 0.15);
        return subtotal+Isv;
    }
    
    public double CantidadNoches(){
        double PrecioNoche; 
        if (cantidadnoches == 1){
            PrecioNoche =0;
        }else{
            
            PrecioNoche = cantidadnoches * precio_cabaña; 
        }
        return PrecioNoche; 
    }
